package com.sist.mongo;

public class WritterVO {
	private int mno;
	private String name;
	private String blist;
	private String ISWN;
	public int getMno() {
		return mno;
	}
	public void setMno(int mno) {
		this.mno = mno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBlist() {
		return blist;
	}
	public void setBlist(String blist) {
		this.blist = blist;
	}
	public String getISWN() {
		return ISWN;
	}
	public void setISWN(String iSWN) {
		ISWN = iSWN;
	}
	
	
}
